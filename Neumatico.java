package Practica8;

public class Neumatico {
    private String marca;
    private int diametro;

    public Neumatico(String marca, int diametro) {
        this.marca = marca;
        this.diametro = diametro;
    }

    public String getMarca() {
        return marca;
    }

    public int getDiametro() {
        return diametro;
    }
    
}
